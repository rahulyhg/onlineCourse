      <h2>Rules for forming Identifiers : </h2>
      <ul><li>A variable name is any combination of 1 to 31 alphabets,
digits or underscores. Some compilers allow variable names
whose length could be up to 247 characters. Still, it would be
safer to stick to the rule of 31 characters. Do not create
unnecessarily long variable names as it adds to your typing
effort.</li>
<li>The first character in the variable name must be an alphabet or
underscore.</li>
<li>No commas or blanks are allowed within a variable name.</li>
<li>No special symbol other than an underscore (as in gross_sal)
can be used in a variable name.<br>
Ex.: si_int ,
m_hra ,
pop_e_89 <br></li>
</ul>
