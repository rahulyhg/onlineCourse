      <h2>Arrays : </h2>
      <p>Arrays a kind of data structure that can store a fixed-size sequential collection of
elements of the same type. An array is used to store a collection of data, but it
is often more useful to think of an array as a collection of variables of the same
type.</p>
<ul><li>All arrays consist of contiguous memory locations. The lowest address
corresponds to the first element and the highest address to the last element. </li></ul>
<h3>Declaring arrays : </h3>
<p>To declare an array in C, a programmer specifies the type of the elements and
the number of elements required by an array as follows:</p> 
<pre>type arrayName [ arraySize ];</pre>
<pre>@-D array : type arrayName [ x ][ y ];</pre>


